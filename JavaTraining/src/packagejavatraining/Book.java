public class Book 
{

	public static void main(String args[]) {
		int iresult, irem;
		double dresult, drem;
		iresult = 10 / 4;
		irem = 10 % 4;
		dresult = 10.0 / 4.0;
		drem = 10.0 % 4.0;
		System.out.println("Result and remainder of 10 / 4: " +
		iresult + " " + irem);
		System.out.println("Result and remainder of 10.0 / 4.0: "
		+ dresult + " " + drem);
		}
	
	
	
	
}
